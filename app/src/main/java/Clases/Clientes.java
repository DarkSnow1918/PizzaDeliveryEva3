package Clases;

public class Clientes {


    private String ramiro,rosa,robert;


    public Clientes() {
        ramiro = "Ramiro";
        rosa ="Rosa";
        robert="Robert";
    }

    public String getRamiro() {
        return ramiro;
    }

    public void setRamiro(String ramiro) {
        this.ramiro = ramiro;
    }

    public String getRosa() {
        return rosa;
    }

    public void setRosa(String rosa) {
        this.rosa = rosa;
    }

    public String getRobert() {
        return robert;
    }

    public void setRobert(String robert) {
        this.robert = robert;
    }
}
